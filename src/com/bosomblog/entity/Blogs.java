package com.bosomblog.entity;

public class Blogs {
    private Integer bid;

    private String bname;

    private String bintro;

    private String bteam;

    private String bslogan;

    private String bcontact;

    public Integer getBid() {
        return bid;
    }

    public void setBid(Integer bid) {
        this.bid = bid;
    }

    public String getBname() {
        return bname;
    }

    public void setBname(String bname) {
        this.bname = bname;
    }

    public String getBintro() {
        return bintro;
    }

    public void setBintro(String bintro) {
        this.bintro = bintro;
    }

    public String getBteam() {
        return bteam;
    }

    public void setBteam(String bteam) {
        this.bteam = bteam;
    }

    public String getBslogan() {
        return bslogan;
    }

    public void setBslogan(String bslogan) {
        this.bslogan = bslogan;
    }

    public String getBcontact() {
        return bcontact;
    }

    public void setBcontact(String bcontact) {
        this.bcontact = bcontact;
    }
}